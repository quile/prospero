=pod

=head1 NAME

Prospero::Manual - A Guide to Using Prospero in Your Applications

=head1 DESCRIPTION

Adding Prospero to a project should be easy - there are very few dependencies
and it doesn't make any assumptions about the structure of your application.
Using the existing plugins, it should be a simple matter to incorporate
Prospero into your application and start reaping the benefits of modular
construction and response-rewinding.

=head1 MANUAL

=over

=item L<Prospero::Manual::Overview>

You should start here to get an idea of what Prospero provides, and
how you can leverage it in your applications.

=item L<Prospero::Manual::Tutorial>

Build a basic application, and see how Prospero fits into your
application structure.

=item L<Prospero::Manual::Cookbook>

Different scenarios that arise and how to handle them with Prospero.


